public class MyMath {
   static int multiple(int a){
        return a*a;
    }
   static int multiple(int a, int b){
        return a*b;
    }

   static double multiple(double a, double b){
        return a*b;
    }
   static double multiple(double a, double b, double c){
        return a*b/c;
    }
}
